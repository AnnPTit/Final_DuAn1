    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        int rowSelected = tblKhachHang.getSelectedRow();
        if (rowSelected != -1) {
            KhachHang kh = listKhachHang.get(rowSelected);
            lbId.setText(kh.getId() + "");
            txtMa.setText(kh.getCode());
            txtTen.setText(kh.getName());
            String sex = (String) tblKhachHang.getValueAt(rowSelected, 3);
            if (sex == "Male") {
                rdoMale.setSelected(true);
            } else {
                rdoFemale.setSelected(true);
            }

            txtDiaChi.setText(kh.getAddress());
            txtSDT.setText(kh.getPhone());
            txtEmail.setText(kh.getEmail());

            String tt = (String) tblKhachHang.getValueAt(rowSelected, 7);
            if (tt == "Active") {
                rdoActive.setSelected(true);
            } else {
                rdoInActive.setSelected(true);
            }
        }
    }//GEN-LAST:event_tblKhachHangMouseClicked

