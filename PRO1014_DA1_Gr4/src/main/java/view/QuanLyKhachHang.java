    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        KhachHang khachHang = validateThongTin();
        if (khachHang == null) {
            return;
        }
        khachHang.setStatus(0);
        GioHang gioHang = gioHangService.getGioHangByKH(Integer.valueOf(lbId.getText()));
        gioHang.setTrangThai(0);
        gioHang.setNgayThanhToan(new Date());

        int rowSelected = tblKhachHang.getSelectedRow();
        if (rowSelected != -1) {
            khachHang.setCode(getCode());
            KhachHang xoa = khachHangService.update(khachHang);
            gioHangService.updateGioHang(gioHang);
            JOptionPane.showMessageDialog(this, "Thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Không có data");
        }
        loadTable();
        reset();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private String getCode() {
        int rowSelected = tblKhachHang.getSelectedRow();
        return (String) tblKhachHang.getValueAt(rowSelected, 1);
    }

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        KhachHang khachHang = validateThongTin();
        if (khachHang == null) {
            JOptionPane.showMessageDialog(this, "Thất bại");
            return;
        }

        String code = getCode();
        khachHang.setCode(code);

        if (khachHangService.update(khachHang) != null) {
            JOptionPane.showMessageDialog(this, "Thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Thất bại");
        }
        loadTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

