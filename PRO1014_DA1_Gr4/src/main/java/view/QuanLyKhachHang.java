    private void searchByCode() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);

        List<KhachHang> list = khachHangService.getAll();
        for (KhachHang kh : list) {
            if (kh.getCode().toLowerCase().contains(txtSearchOnKey.getText().trim().toLowerCase())) {
                model.addRow(kh.toDataRow());
            }
        }
    }

    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        listKhachHang = khachHangService.getAll();
        for (KhachHang kh : listKhachHang) {
            model.addRow(new Object[]{
                kh.getId(), kh.getCode(), kh.getName(), kh.getSex() == true ? "Male" : "Female",
                kh.getAddress(), kh.getPhone(), kh.getEmail(), kh.getStatus() == 1 ? "Active" : "InActive"
            });
        }
    }

    private KhachHang validateThongTin() {
        KhachHang khachHang = new KhachHang();
        boolean check = true;
        String ma = null;
        try {
            ma = txtMa.getText();
            listKhachHang = khachHangService.getAll();
            if (listKhachHang.size() == 0) {
                ma = "KH0";
            } else {
                KhachHang khId = listKhachHang.get(0);
                ma = "KH" + (khId.getId() + 1);
            }

        } catch (Exception e) {
            check = false;
        }

        String ten = null;
        try {
            ten = txtTen.getText();
            if (ten.isBlank()) {
                msgTen.setText("Error!");
                msgTen.setForeground(Color.red);
                check = false;
            }
        } catch (Exception e) {
            check = false;
        }

        boolean sex;
        if (rdoMale.isSelected()) {
            sex = true;
        } else {
            sex = false;
        }

        String diaChi = null;
        try {
            diaChi = txtDiaChi.getText();
            if (diaChi.isBlank()) {
                msgDiaChi.setText("Error");
                msgDiaChi.setForeground(Color.red);
                check = false;
            }
        } catch (Exception e) {
            check = false;
        }

        String sdt = null;
        try {
            sdt = txtSDT.getText();
            if (sdt.isBlank()) {
                msgSdt.setText("Error");
                msgSdt.setForeground(Color.red);
                check = false;
            }

            if (txtSDT.getText().length() > 10 || txtSDT.getText().length() < 10) {
                msgSdt.setText("Nhập thiếu SĐT");
                msgSdt.setForeground(Color.red);
                check = false;
            }
        } catch (Exception e) {
            check = false;
        }

        String email = txtEmail.getText();

        Integer trangThai = 1;

        if (check == true) {
            //   khachHang = new KhachHang(ma, ten, sex, diaChi, sdt, email, trangThai,null);
            khachHang.setCode(ma);
            khachHang.setName(ten);
            khachHang.setSex(sex);
            khachHang.setAddress(diaChi);
            khachHang.setPhone(sdt);
            khachHang.setEmail(email);
            khachHang.setStatus(trangThai);
            return khachHang;
        } else {
            return null;
        }

    }

